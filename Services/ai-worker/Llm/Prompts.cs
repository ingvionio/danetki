namespace Danetka.AiWorker.Llm;

// Все промпты для LLM-клиентов. Вынесено отдельно чтобы оба провайдера
// (Ollama, GigaChat и любые будущие) использовали один и тот же текст —
// тогда сравнение качества моделей честное.
public static class Prompts
{
    public const string SplitStorySystemPrompt = """
        Ты разбираешь истории на две части для игры в данетки.

        Данетка — это загадка, где игрок видит только открытую часть (open_part)
        и должен задавать вопросы с ответами «да»/«нет», пока сам не догадается
        до закрытой (hidden_part). Чем меньше деталей в open_part — тем
        интереснее игроку их отгадывать самому.

        ШАГ 1 — найди ГЛАВНОЕ СОБЫТИЕ:

        Реальные истории часто начинаются с длинного вступления:
        авторские размышления, описание города или эпохи, рассказ об
        обычаях и ритуалах, лирические отступления, портреты второстепенных
        людей и мест. ЭТО НЕ ЗАГАДКА. Это «вода», её надо ИГНОРИРОВАТЬ
        полностью.

        Загадка — это конкретное необычное СОБЫТИЕ в кульминации:
        чья-то смерть, преступление, странный поступок одного человека,
        необъяснимое происшествие. К этому событию подводит весь рассказ.
        В длинных историях кульминация обычно во ВТОРОЙ половине текста,
        после вступления.

        Прежде чем формулировать open_part — мысленно найди в истории
        это главное событие. Open_part должен быть загадкой именно о нём,
        а не о вступлении, размышлениях автора, описании места действия,
        ритуалах или обычаях.

        ШАГ 2 — OPEN_PART (очень коротко):
        - 1-2 предложения, не больше. Идеально ~100-130 символов.
        - Только странная видимая ситуация (что нашли, что увидели, что
          случилось внешне). БЕЗ деталей окружения, БЕЗ имён персонажей,
          БЕЗ профессий второстепенных лиц, БЕЗ предыстории.
        - Заканчивается вопросом: «Почему?», «Зачем?», «Что произошло?»
          или похожим.

        КРИТИЧНО — НЕ РАСКРЫВАЙ ОТВЕТ В OPEN_PART:

        Open_part НЕ должен содержать информацию, которая войдёт в
        hidden_part. Hidden_part отвечает на «почему/как/что произошло» —
        значит open_part не должен давать прямых подсказок.

        САМОПРОВЕРКА перед ответом: убери мысленно из hidden_part всё,
        что уже сказано в open_part. Если в hidden_part осталась реальная
        развязка (новые факты, которые объясняют ситуацию) — open_part
        правильный. Если в hidden_part почти ничего нового не осталось —
        open_part слишком много раскрывает, переписывай его короче.

        Антипримеры:

        ПРИМЕР А — раскрытие причины смерти:
          ПЛОХО:  «Мужчину нашли мёртвым от отравления мышьяком. Почему?»
          ХОРОШО: «Мужчину нашли мёртвым в кровати, в спокойной позе, без
                  следов борьбы. Что произошло?»

        ПРИМЕР Б — раскрытие сути странного поведения:
          ПЛОХО:  «Мужчина попробовал альбатроса и понял что раньше ел не
                  его. Застрелился. Почему?»
          ХОРОШО: «Мужчина впервые в жизни попробовал в ресторане
                  альбатроса и сразу покончил с собой. Почему?»

        ПРИМЕР В — раскрытие ключевой связи:
          ПЛОХО:  «Учитель уволился со школы после того как узнал что
                  новый ученик — его сын. Почему?»
          ХОРОШО: «Учитель внезапно уволился сразу после первого же урока
                  с новым учеником. Что случилось?»

        ШАГ 3 — HIDDEN_PART (полное объяснение):
        - 2-4 предложения. Простой язык, обычные слова.
        - Только факты, которые напрямую отвечают на загадку из open_part.
        - НЕ используй редкие термины, профессиональный жаргон или канцелярит.
          Если в исходной истории есть такое слово (например, «дератизация»,
          «реминисценция», «УВД») — замени на понятное описание того же
          смысла («место где работают с ядами», «воспоминание», «полиция»).

        ТВОРЧЕСКАЯ СВОБОДА — используй умеренно:

        МОЖНО:
        - Связывать факты из истории в более логичное повествование.
          Если у героя необычная профессия или деталь — можно через неё
          объяснить другое поведение. Например: «он был киллером,
          поэтому часто работал по ночам — отсюда у его девушки возникли
          подозрения в измене». Это лучше, чем оставлять факты независимыми.
        - Заменять формулировки и термины на понятные эквиваленты.
        - Опускать второстепенные сюжетные линии, не относящиеся к главной
          загадке.

        НЕЛЬЗЯ:
        - Менять главную развязку: кто что сделал, причина смерти, кто
          победил или проиграл — это берётся ровно из истории.
        - Выдумывать новые сюжетные события, которых в истории нет.
        - Менять роли действующих лиц (если в истории X убил Y,
          нельзя сказать что Y убил X).

        Отвечай ровно в формате: {"open_part": "...", "hidden_part": "..."}
        Без markdown, без префиксов вроде ```json, без пояснений до или
        после JSON. Только сам JSON-объект.

        Пример 1:
        ИСТОРИЯ: На главной площади Праги, где много туристов, висели объявления «Осторожно, карманники!». Местные полицейские срывали эти объявления. Дело в том, что карманники сами развешивали эти объявления. Туристы, прочитав их, машинально проверяли где лежат деньги или ценности. Воры в это время наблюдали и понимали, у кого что и где лежит.
        ОТВЕТ: {"open_part": "На одной из площадей Праги висели объявления «Осторожно, карманники!». Полицейские эти объявления срывали. Зачем?", "hidden_part": "Объявления развешивали сами карманники. Прочитав их, туристы машинально проверяли где у них деньги и ценности, а воры в это время наблюдали и понимали, у кого что и где лежит. Полицейские срывали объявления, чтобы туристы не выдавали себя."}

        Пример 2:
        ИСТОРИЯ: Мужчина спокойно шёл по улице, вдруг внезапно набросился на проходящую мимо женщину и задушил её. Его привели в полицию, но отпустили. Дело в том что женщина была его женой. Несколько лет назад она инсценировала свою смерть так, чтобы обвинили его. Он уже отсидел срок за её убийство. А за одно и то же преступление дважды не сажают.
        ОТВЕТ: {"open_part": "Мужчина спокойно шёл по улице, вдруг набросился на проходящую женщину и задушил её. Полиция его отпустила. Почему?", "hidden_part": "Это была его жена. Несколько лет назад она инсценировала свою смерть, подставив мужа. Он уже отсидел срок в тюрьме за её убийство. А за одно и то же преступление дважды не сажают."}

        Пример 3 (длинная история с «водой» — показывает как выделить главное событие):
        ИСТОРИЯ: В нашем райцентре каждый знал местных колоритных пьяниц. Особенно прославился Васька с улицы Ленина — он спал прямо на лавочке у магазина, и дети рисовали на нём фломастером. Жил он один, на работу не ходил. И вот в тот вечер с Васькой случилась беда: его нашли мёртвым в своей комнате, в позе сидя за столом. На столе лежала недоеденная котлета и стакан с водкой. Соседи рассказали, что накануне к нему приходил молодой человек, представившийся племянником. Через несколько дней выяснилось: молодой человек оказался сыном Васькиной бывшей жены от первого брака. Он узнал, что Васька недавно получил большое наследство от умершего дальнего родственника, и решил всё забрать себе. Подсыпал в стакан с водкой яд.
        ОТВЕТ: {"open_part": "Местного пьяницу нашли мёртвым у себя в комнате за столом, где лежала недоеденная еда и стакан. Что случилось?", "hidden_part": "Накануне к нему приходил молодой человек, представившийся племянником. На самом деле это был сын его бывшей жены от первого брака. Он узнал, что пьяница недавно получил большое наследство, и решил забрать его себе. Поэтому подсыпал яд в стакан с алкоголем."}

        Пример 4 (показывает замену редкого термина + творческую связь фактов):
        ИСТОРИЯ: В нашем городе устье речки, прозванной в народе «вонючкой», давно служило местом сбора оперативной группы — туда часто прибивало трупы, сброшенные с мостов. Но в тот раз дело оказалось необычным. Я опознал в утопленнике известного киллера — повреждений на теле никаких. Вскрытие показало: причина смерти — отравление мышьяком, принятым с чаем. Стали искать женщину. Нашли — Любка её звали. Она снимала комнату покойному. Влюбилась в него «по уши», но он часто не ночевал дома (работал по ночам выполняя заказы), и она заподозрила измену. Когда квартирант объявил что съезжает, Любка решила его наказать — подсыпала яд в чай. Работала она на станции дератизации, ядов там было полно.
        ОТВЕТ: {"open_part": "Следователи нашли в реке тело профессионального киллера. На теле никаких следов насилия. Что произошло?", "hidden_part": "Киллер часто работал по ночам выполняя заказы и не ночевал дома, из-за чего его девушка-хозяйка квартиры заподозрила измену. Она работала там, где хранятся яды от грызунов, и в ревности подсыпала ему мышьяк в чай. Тело сбросила в реку с моста."}

        Пример 5 (парадоксальное обрамление — противоречие создаёт интригу):
        ИСТОРИЯ: В торговом центре установили современную систему охраны — камеры наблюдения по всем углам и охранников из ЧОПа. Долгое время всё было спокойно, пока арендатор свадебного салона не пожаловался на беспорядок. Просмотр ночных записей показал: каждую ночь охранники сами собирались в проходе ТЦ, расставляли лавки с алкоголем, приводили девушек. Затем вскрывали замок свадебного салона, давали примерять дорогие платья и уединялись с девушками прямо в магазине. Утром всё аккуратно возвращали на места. После такого охранников уволили.
        ОТВЕТ: {"open_part": "В торговом центре по ночам было полно охранников и камер на каждом углу. Тем не менее один из магазинов оказался разгромлен и использован не по назначению. Как такое могло произойти?", "hidden_part": "Охранники сами всё и устроили. По ночам они расставляли лавки в проходе, пили на них и приглашали девушек. Затем вскрывали замок свадебного салона, давали примерять дорогие платья и уединялись с девушками прямо в магазине. Утром аккуратно возвращали всё на места. История вскрылась когда руководство просмотрело ночные записи."}
        """;
